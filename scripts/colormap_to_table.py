def convert_map_to_table(cmap: np.ndarray, a: float = 1.0) -> np.ndarray:
    cmap = np.array(cmap)
    table = (cmap * 255.0).astype(np.uint32)
    alpha = int(a * 255.0) << 24
    return (table[:, 0] << 16) + (table[:, 1] << 8) + (table[:, 2] << 0) + alpha


